package crackingTheCodingInterview.LinkedLists;

public class SinglyLinkedListIntVal {
	private SinglyLinkedListIntVal next;
	private int value;
	
	public SinglyLinkedListIntVal() {
		super();
	}

	public SinglyLinkedListIntVal getNext() {
		return next;
	}
	
	public void setNext(SinglyLinkedListIntVal next) {
		this.next = next;
	}
	
	public int getValue() {
		return value;
	}
	
	public void setValue(int value) {
		this.value = value;
	}
}
